#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{

    // Get reference to the selected cell
    UITableViewCell *selectedCell = [tableView cellForRowAtIndexPath:indexPath];
    
    // Get the detail from the selected cell
    NSString *theDetail = selectedCell.textLabel.text;
    
    // Call the delegate method to update the detail view controller,
    // passing in the detail from the selected cell
    [delegate updateDetailViewWith:theDetail];
    
}

